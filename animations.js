    function escapeHtml(s) {
      return String(s || '').replace(/[&<>"']/g, c => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' }[c]));
    }

    function readProgFromDom() {
      const desc = document.getElementById('prog-desc')?.value || '';
      const tagliaMaglia = document.getElementById('prog-taglia')?.value || '';
      const anni = [...document.querySelectorAll('#prog-years .year-card')].map(card => {
        const get = f => card.querySelector('[data-f="' + f + '"]')?.value || '';
        return { anno: get('anno'), f1: get('f1'), f2: get('f2'), f3: get('f3'), f4: get('f4') };
      });
      const readVdb = (selector) => [...document.querySelectorAll(selector + ' .vdb-card')].map(card => {
        const get = f => card.querySelector('[data-vf="' + f + '"]')?.value || '';
        return { anno: get('anno'), testo: get('testo') };
      });
      const vdbInverno = readVdb('#prog-vdbInv');
      const vdbEstate = readVdb('#prog-vdbEst');
      return { desc, anni, orme: [], tagliaMaglia, vdbInverno, vdbEstate };
    }

    function removeProgYear(idx) {
      const s = window._currentSocio;
      if (!s) return;
      if (!confirm('Eliminare questo anno dalla scheda?')) return;
      const data = readProgFromDom();
      data.anni.splice(idx, 1);
      saveProgressione(s, data);
      document.getElementById('m-body').innerHTML = renderModalTab_progressione(s);
      bindProgressioneHandlers(s);
    }

    function bindProgressioneHandlers(s) {
      const root = document.getElementById('m-body');
      const selector = 'textarea, input.year-input, input.vdb-input, #prog-taglia';
      root.querySelectorAll(selector).forEach(el => {
        const ev = el.tagName === 'SELECT' ? 'change' : 'input';
        el.addEventListener(ev, () => {
          const data = readProgFromDom();
          saveProgressione(s, data);
        });
      });
    }

    function setModalTab(tab) {
      document.querySelectorAll('.modal-tab').forEach(t => {
        t.classList.toggle('active', t.dataset.mtab === tab);
      });
      const s = window._currentSocio, u = window._currentUnita;
      if (!s) return;
      const body = document.getElementById('m-body');
      if (tab === 'progressione') {
        body.innerHTML = renderModalTab_progressione(s);
        if (s.BrancaKey !== 'lupetti') bindProgressioneHandlers(s);
      } else {
        body.innerHTML = renderModalTab_anagrafica(s, u);
      }
    }

