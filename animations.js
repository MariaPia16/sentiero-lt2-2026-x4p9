    function sortByCognome(arr) {
      return arr.slice().sort((a, b) => {
        const cA = (a.Cognome || '').toUpperCase(), cB = (b.Cognome || '').toUpperCase();
        if (cA !== cB) return cA < cB ? -1 : 1;
        const nA = (a.Nome || '').toUpperCase(), nB = (b.Nome || '').toUpperCase();
        return nA < nB ? -1 : nA > nB ? 1 : 0;
      });
    }

    function sortSquadrigliaByRuolo(arr) {
      function rank(s) {
        const r = (s.Ruolo || '').toLowerCase();
        if (r.startsWith('capo squadriglia')) return 0;
        if (r.startsWith('vice capo squadriglia')) return 2;
        return 1;
      }
      return arr.slice().sort((a, b) => {
        const ra = rank(a), rb = rank(b);
        if (ra !== rb) return ra - rb;
        const cA = (a.Cognome || '').toUpperCase(), cB = (b.Cognome || '').toUpperCase();
        if (cA !== cB) return cA < cB ? -1 : 1;
        const nA = (a.Nome || '').toUpperCase(), nB = (b.Nome || '').toUpperCase();
        return nA < nB ? -1 : nA > nB ? 1 : 0;
      });
    }

