    function splitPrede(text) {
      if (!text) return { parts: [], acc: '' };
      let acc = '';
      const m = text.match(/^(.*?)\s*-\s*([A-Za-z\/?]+\??)\s*$/);
      let body = text;
      if (m) { body = m[1]; acc = m[2]; }
      const parts = body.split('/').map(p => p.trim()).filter(Boolean);
      return { parts, acc };
    }

