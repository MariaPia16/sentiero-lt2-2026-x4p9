    function aggregaSpecialitaLupetto(s) {
      const key = lupettoKey(s);
      const out = [];
      const seen = new Set();
      const add = (nome) => {
        if (!nome) return;
        const n = String(nome).trim();
        if (!n) return;
        const low = n.toLowerCase();
        if (!seen.has(low)) { seen.add(low); out.push(n); }
      };
      const d26 = (typeof LUPETTI_DATA !== 'undefined') ? LUPETTI_DATA[key] : null;
      const d25 = (typeof LUPETTI_DATA_2425 !== 'undefined') ? LUPETTI_DATA_2425[key] : null;
      if (d26 && Array.isArray(d26.specialita)) d26.specialita.forEach(sp => add(sp.nome));
      if (d25 && Array.isArray(d25.specialita)) d25.specialita.forEach(sp => add(sp.nome));
      // capitalizzo prima lettera
      return out.map(n => n.charAt(0).toUpperCase() + n.slice(1));
    }

